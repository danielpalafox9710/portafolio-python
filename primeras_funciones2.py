def suma(num1, num2):	#definicion de funcion con 2 variables parametro

	resultado=num1+num2

	return resultado


print (suma(5,7))	#llamado de función con parametros (num1=5,num2=7)

print (suma(2,3))	#llamado de función con parametros

print (suma (35,36))#llamado de función con parametros

almacena_resultado=suma(5,8)

print(almacena_resultado)


